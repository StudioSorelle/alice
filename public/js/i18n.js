  var _lang = localStorage.getItem('alice_lang') || 'en';
  window.currentLang = _lang;

  window.t = function (key, vars) {
    var str = (TRANSLATIONS[window.currentLang] && TRANSLATIONS[window.currentLang][key])
      || TRANSLATIONS['en'][key] || key;
    if (vars) {
      Object.keys(vars).forEach(function (k) { str = str.replace('{' + k + '}', vars[k]); });
    }
    return str;
  };

  window.setLang = function (lang) {
    if (lang !== 'en' && lang !== 'nl') return;
    window.currentLang = lang;
    localStorage.setItem('alice_lang', lang);
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      var txt = window.t(key);
      if (el.placeholder !== undefined && el.tagName === 'INPUT') el.placeholder = txt;
      else el.textContent = txt;
    });
    var btn = document.getElementById('langToggle');
    if (btn) btn.textContent = lang === 'nl' ? 'EN' : 'NL';
  };
